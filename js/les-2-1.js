//создаём массив
var animals = ['cat', 'dog', 'hours']
alert(animals)

// push() - доб. элемент в конец массива
animals.push('fish')
alert(animals)

// unshift() - доб. элемент в НАЧАЛО массива
animals.unshift('Goose')
alert(animals)

// pop() - доб. удаляет элю из КОНЦА массива
animals.pop()
alert(animals)

// shift() - удаляет ел. из НАЧАЛА массива
animals.shift()
alert(animals)